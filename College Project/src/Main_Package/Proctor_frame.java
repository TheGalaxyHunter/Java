    private void update_studActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_studActionPerformed
        // TODO add your handling code here:
        int index = 0;
        set_disable();
        this.update_stud.setEnabled(true);
        
        if(this.update_stud.getLabel().equals("Update")) {
            index = this.usn.getSelectedIndex();
            
            p.studName = this.stud_name.getText();
            p.usn = this.stud_usn.getText();
            p.dept = this.stud_dept.getText();
            p.sem = Integer.parseInt(this.stud_sem.getText());
            p.attendance = Integer.parseInt(this.attendance.getText());
        
            p.update(p.usn);
            JOptionPane.showMessageDialog(this, "Student updated succefully");
            
            set_textfield_disable();
            this.update_stud.setLabel("Update Student");
            set_enable();
            
            String[] stud_usn = p.listStudents();
            this.usn.removeAllItems();
            for(String x:stud_usn) {
            this.usn.addItem(x);
            }
            
            this.usn.setSelectedIndex(index);
        }
        else {
            this.update_stud.setLabel("Update");
            set_textfield_enable();
            this.stud_usn.setEditable(false);
        }
    }//GEN-LAST:event_update_studActionPerformed

