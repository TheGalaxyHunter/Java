    private void usnItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_usnItemStateChanged
        // TODO add your handling code here:
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            String selected_usn = usn.getSelectedItem().toString();
            p.usn = selected_usn;
            p.getDetails();
            
            this.stud_usn.setText(p.usn);
            this.stud_name.setText(p.studName);
            this.stud_dept.setText(p.dept);
            this.stud_sem.setText(p.sem + "");
            this.attendance.setText(p.attendance + "");
            
            String[] sub = p.displayMarks(p.usn, p.dept, p.sem);
            if(sub != null) 
                for(int i=0; i<sub.length; i=i+2) {
                    System.out.println(sub[i] + " 0 " + i);
                    System.out.println(sub[i+1] + " 1 " + i);
                    this.studMarks.setValueAt(sub[i], 0, i/2);
                    this.studMarks.setValueAt(sub[i+1], 1, i/2);
                }
            
            
            this.update_stud.setVisible(true);
            this.delete_stud.setVisible(true);
        }
    }//GEN-LAST:event_usnItemStateChanged

